export const GLYPH_W = 5
export const GLYPH_H = 7
export const TRACKING = 1 // grid cells between glyphs

// Returns { rects: [{x,y,w,h}], w, h } in grid units.
export function textToRects(text) {
  const chars = [...text.toUpperCase()].filter((c) => GLYPHS[c])
  if (chars.length === 0) return { rects: [], w: GLYPH_W, h: GLYPH_H }
  const rects = []
  let cursor = 0
  for (const ch of chars) {
    const rows = GLYPHS[ch]
    for (let y = 0; y < GLYPH_H; y++) {
      const row = rows[y]
      let run = -1
      for (let x = 0; x <= GLYPH_W; x++) {
        const on = x < GLYPH_W && row[x] === '1'
        if (on && run < 0) run = x
        if (!on && run >= 0) {
          rects.push({ x: cursor + run, y, w: x - run, h: 1 })
          run = -1
        }
      }
    }
    cursor += GLYPH_W + TRACKING
  }
  return { rects, w: cursor - TRACKING, h: GLYPH_H }
}
